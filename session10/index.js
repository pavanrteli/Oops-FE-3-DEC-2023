// ------ Directory Commands

// 1. mkdir - creats a directory
// 2. pwd - print working directory - in which FOLDER (DIRECTORY) you are working.
// 3. cd - change directory - to go from one FOLDER (DIRECTORY) to ANOTHER.
// 4. ls - list down - to list all the elements inside that Perticular FOLDER (DIRECTORY).
// 5. cd .. - take you to the parent
// 6. rmdir - removes empty directory
// 7. rm -r folderName - remove the non empty directory also.

